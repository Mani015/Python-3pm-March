# Update:update()	Updates the dictionary with the specified key-value pairs


d1 = {'apple':10,'mango':20,'banana':5,'graphes':30}
print(d1)
# {'apple': 10, 'mango': 20, 'banana': 5, 'graphes': 30}
print()
d1.update({'orange':40})
print(d1)
# {'apple': 10, 'mango': 20, 'banana': 5, 'graphes': 30, 'orange': 40}


d1.update({'pineapple':100,'straberry':50})
print(d1)
# {'apple': 10, 'mango': 20, 'banana': 5, 'graphes': 30, 'orange': 40, 'pineapple': 100, 'straberry': 50}




