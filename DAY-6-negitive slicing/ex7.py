
x = [-1,3,4,22,6,7,34,56,90,12,3,5,8,14,50,-2,-4,-5,0]
print(x)
# [-1, 3, 4, 22, 6, 7, 34, 56, 90, 12, 3, 5, 8, 14, 50, -2, -4, -5, 0]
x.sort()
print(x)
# [-5, -4, -2, -1, 0, 3, 3, 4, 5, 6, 7, 8, 12, 14, 22, 34, 50, 56, 90]

x.sort(reverse=False)
print(x)
# [-5, -4, -2, -1, 0, 3, 3, 4, 5, 6, 7, 8, 12, 14, 22, 34, 50, 56, 90]


x.sort(reverse=True)
print(x)
# [90, 56, 50, 34, 22, 14, 12, 8, 7, 6, 5, 4, 3, 3, 0, -1, -2, -4, -5]

