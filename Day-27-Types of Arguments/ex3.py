#Keyword Arguments
# A keyword argument is an argument value, passed to function preceded by the variable name and an equals sign.


def Keyword(x,y):
    print('x is :', x)
    print('y is :', y)

# Keyword(y =10,x=20)
# x is : 20
# y is : 10



x = 150
y = 123

Keyword(x=y,y=y)
# x is : 123
# y is : 123

